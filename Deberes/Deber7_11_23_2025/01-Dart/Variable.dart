void main() {
  // Strings (Cadenas de texto)
  String myString = "Cadena de texto";
  myString = "Se ha modificado la cadena";
  print(myString);
  
  // Integers (Enteros)
  var myInt = 7;
  myInt = myInt + 4;
  print(myInt);
  
  // Doubles (Decimales)
  double myDouble = 6.5;
  print(myDouble);

  // Booleans (Verdadero/Falso)
  bool myBool = true;
  myBool = false;
  print(myBool);
}
